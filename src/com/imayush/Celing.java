package com.imayush;

public class Celing {
    private int height ;
    private String paintedColour ;

    public Celing(int height, String paintedColour) {
        this.height = height;
        this.paintedColour = paintedColour;
    }

    public int getHeight() {
        return height;
    }

    public String getPaintedColour() {
        return paintedColour;
    }
}
